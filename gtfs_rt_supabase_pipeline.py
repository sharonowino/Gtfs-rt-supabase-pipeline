# 1.  UTILITY HELPERS


def ts_to_local(timestamp, timezone: str = TIMEZONE) -> Optional[str]:
    """Convert a Unix timestamp (int/float) to a local datetime string."""
    if timestamp is None or (isinstance(timestamp, float) and math.isnan(timestamp)):
        return None
    try:
        dt = pd.to_datetime(timestamp, unit="s", utc=True, errors="coerce")
        if pd.isna(dt):
            return None
        return dt.tz_convert(timezone).tz_localize(None).strftime("%Y-%m-%d %H:%M:%S")
    except Exception:
        return None


def businessday_to_datetime(date: str, time_str: str) -> Optional[datetime]:
    """Parse GTFS service-date + extended time (hours ≥ 24 allowed)."""
    try:
        base = datetime.strptime(date, "%Y%m%d")
        h, m, s = map(int, time_str.split(":"))
        return base + timedelta(days=h // 24, hours=h % 24, minutes=m, seconds=s)
    except Exception:
        return None


def clean_text(text: str) -> str:
    """Lightweight text cleaner for alert descriptions."""
    if not text:
        return ""
    text = re.sub(r"<[^>]+>", " ", str(text))
    text = re.sub(r"http\S+|www\.\S+", " ", text)
    text = re.sub(r"[^\w\sÀ-ÿ]", " ", text)
    return re.sub(r"\s+", " ", text).strip().lower()


def get_first_translation(field) -> Optional[str]:
    """Extract the first translation text from a GTFS-RT TranslatedString."""
    if field and hasattr(field, "translation") and len(field.translation) > 0:
        return field.translation[0].text
    return None


