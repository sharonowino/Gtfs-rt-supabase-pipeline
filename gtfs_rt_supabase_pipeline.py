# 2.  FEED FETCHING


def fetch_feed(feed_name: str, url: str) -> Optional[gtfs_realtime_pb2.FeedMessage]:
    """Download and parse a single GTFS-RT protobuf feed."""
    try:
        resp = requests.get(url, timeout=30)
        resp.raise_for_status()
        feed = gtfs_realtime_pb2.FeedMessage()
        feed.ParseFromString(resp.content)
        log.info("Fetched %s (%d entities)", feed_name, len(feed.entity))
        return feed
    except Exception as exc:
        log.error("Error fetching %s: %s", feed_name, exc)
        return None


def fetch_all_feeds() -> Dict[str, Optional[gtfs_realtime_pb2.FeedMessage]]:
    return {name: fetch_feed(name, url) for name, url in GTFS_RT_FEEDS.items()}



